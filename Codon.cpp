///*******************************************
void CodonsTable:: LoadCodonsFromFile(string codonsFileName)
{
    ifstream filename;
    filename.open("codonsFileName");
    string line = "";
    //cout << line << endl;
    if( filename.good())
    {
        int counter = 0;
        //cin.ignore();
        while(getline(filename, line))
        {
            //cout << line << endl;
            for(int i = 0; i < 3; ++i )
            {
                codons[counter].value += line[i];
            }
            // cout<<codons[counter].value << "    ";
            codons[counter].AminoAcid = line[4];
            counter++;
            line = "";
        }

    }

    filename.close();
}
///*************************************************
char CodonsTable:: getAminoAcid(string value)
{
    for(int i = 0; i < 64; ++i)
    {
        if(value == codons[i].value)
        {
            return codons[i].AminoAcid;
        }
    }
}
///*************************************************
void CodonsTable::setCodon(string value, char AminoAcid, int index)
{
    codons[index].value = value;
    codons[index].AminoAcid = AminoAcid;
}
